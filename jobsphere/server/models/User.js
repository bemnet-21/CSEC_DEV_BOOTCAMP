import mongoose, { Schema } from "mongoose";
import { type } from "os";

const userSchema = new Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ["recruiter", "talent"],
        required: true
    }
})

export default mongoose.model('User', userSchema)